/////////////////以下为写汉字函数//////////////////////////////////////////////////////
void Draw_One_Hz(int x,int y, int col,uchar HHzz)
{
	uchar dat;
	int i;
	
	for(i=0;i<8;i++)
	{
		dat=HHzz & 0x80;

		if(dat==0x80)
		{
			putPixelScreen(col,x+i,y);
		}
		else
		{
			putPixelScreen(ARGB(255,222,207,168),x+i,y);
		}

		HHzz=HHzz<<1;
	}
}

void Draw_Hz_16(int x, int y, int col, uchar Hz[])
{
	int i,j;
	for(i=0;i<16;i++)
	{
		for(j=0;j<2;j++)
		{
			Draw_One_Hz(x+8*j,y+i,col,Hz[2*i+j]);
		}
	}  
}

void Draw_Hz_24(int x, int y, int col, uchar Hz[])
{
	int i,j;
	for(i=0;i<24;i++)
	{
		for(j=0;j<3;j++)
		{
			Draw_One_Hz(x+8*j,y+i,col,Hz[3*i+j]);
		}
	}
}

